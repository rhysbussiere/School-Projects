CREATE TABLE Library_Branch (
	ID INT PRIMARY KEY NOT NULL IDENTITY (1,1),
	Name VARCHAR(50) NOT NULL,
	Address VARCHAR(50) NOT NULL
);

CREATE TABLE Borrower (
	Card_Number INT PRIMARY KEY NOT NULL IDENTITY (5000,1),
	Name VARCHAR(50) NOT NULL,
	Address VARCHAR(50) NOT NULL,
	Phone VARCHAR(50) NOT NULL,
);

CREATE TABLE Publisher (
	Company VARCHAR(50) PRIMARY KEY NOT NULL,
	Address VARCHAR(50) NOT NULL,
	Phone VARCHAR(50) NOT NULL
);

CREATE TABLE Book (
	ID INT PRIMARY KEY NOT NULL IDENTITY (100,1),
	Title VARCHAR(50) NOT NULL,
	Publisher VARCHAR(50) FOREIGN KEY REFERENCES Publisher(Company)
);

CREATE TABLE Author (
	BookID INT FOREIGN KEY REFERENCES Book(ID),
	Name VARCHAR(50) NOT NULL
);

CREATE TABLE Loans (
	BookID INT FOREIGN KEY REFERENCES Book(ID),
	ID INT FOREIGN KEY REFERENCES Library_Branch(ID),
	CardNumber INT FOREIGN KEY REFERENCES Borrower(Card_Number),
	Date_Out DATE NOT NULL,
	Due_Date DATE NOT NULL
);

CREATE TABLE Book_Copies (
	Copies VARCHAR(50) NOT NULL,
	BookID INT FOREIGN KEY REFERENCES Book(ID),
	Library_BranchID INT FOREIGN KEY REFERENCES Library_Branch(ID)
);

